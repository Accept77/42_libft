#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*tmp;

	tmp = malloc(size * count);
	if (!(tmp))
		return (NULL);
	ft_bzero(tmp, (count * size));
	return (tmp);
}
