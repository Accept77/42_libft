#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*result;
	size_t	len_s;
	size_t	i;

	len_s = ft_strlen(s);
	i = 0;
	result = (char *)malloc(sizeof(char) * (len_s + 1));
	if (!result || !s)
		return (NULL);
	while (s[i])
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
